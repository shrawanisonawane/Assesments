#include <iostream>
using namespace std;
int main() {
int x = 35, y = 110;
cout << "x == y: " << (x == y) << endl;
cout << "x != y: " << (x != y) << endl;
cout << "x < y: " << (x < y) << endl;
cout << "x > y: " << (x > y) << endl;
cout << "x <= y: " << (x <= y) << endl;
cout << "x >= y: " << (x >= y) << endl;
return 0;
}