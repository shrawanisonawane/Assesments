#include <iostream> // Preprocessor directive ('#' is punctuation)
using namespace std; // 'using' is a keyword
int main() { // 'int' is a keyword, 'main' is an identifier
int age = 21; // 'int' is a keyword, '25' is a literal
float height = 6.1; // '=' and ';' are operators and punctuation
cout << "Age: " << age;
return 0; // 'return' is a keyword
}